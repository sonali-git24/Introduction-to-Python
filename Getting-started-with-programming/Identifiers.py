#In Python, an identifier is a name given to a programming element, such as a variable, function, class, or module. It allows programmers to uniquely identify and refer to these elements within their code. Identifiers must adhere to specific rules:

#They can consist of letters (A-Z, a-z), digits (0-9), and underscores (_).
#They must begin with a letter or an underscore, not a digit.
#They cannot be a reserved keyword in Python (e.g., if, for, while, class).
#They are case-sensitive (myVar and myvar are different identifiers).
#Special characters (e.g., $, @, %) are not allowed.

# Valid identifiers
my_variable = 10
_my_variable = 20
myVariable = 30
MYVARIABLE = 40

# Invalid identifiers
# Cannot start with a digit -------------->1_variable = 50 
# Cannot contain special characters---------------->my-variable = 60 
# Cannot be a reserved keyword----------------->if = 70 

# All Variables are Identifiers but all identifiers can't be a variable!